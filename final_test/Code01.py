# 문자열을 입력받아 반대로 출력하는 코드
inStr, outStr = '', ''
count, i = 0, 0

inStr = input("문자열 입력 : ")
count = len(inStr)

for i in range(0, count):
    outStr += inStr[count - (i+1)]

print("내용을 반대로 출력 : " + outStr)
